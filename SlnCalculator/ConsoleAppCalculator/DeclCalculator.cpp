#include "DeclCalculator.h"

float DeclCalculator::GetAddition(float arg1, float arg2)
{
	float addition = arg1 + arg2;
	return addition;
}
float DeclCalculator::GetDivision(float arg1, float arg2)
{
	float division = arg1 / arg2;
	return division;
}
float DeclCalculator::GetMultiplication(float arg1, float arg2)
{
	float multiplication = arg1 * arg2;
	return multiplication;
}
float DeclCalculator::GetSubtraction(float arg1, float arg2)
{
	float subtraction = arg1 - arg2;
	return subtraction;
}